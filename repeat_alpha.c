#include <unistd.h>

int main(int ac, char *av[])
{
	int i;
	int r;

	if(ac == 2)
	{
		i =0;
		r = 0;
		while(av[1][i] >= 'a' && av[1][i] <= 'z')
		r = av[1][i] - 'a' + 1;
		
		while(av[1][i] >= 'A' && av[1][i] <= 'Z')
		r = av[1][i] - 'A' + 1;

		while(r-- > 0)
			write(1, &av[1][i], 1); 	
	}
	write(1, "\n", 1);
	return 0;
}
